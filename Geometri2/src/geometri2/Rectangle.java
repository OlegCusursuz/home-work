
package geometri2;


public class Rectangle {
    
    public int calculateSquareSurface(int sideL, int sideR){
        int surface = sideL * sideR;
        System.out.println("Surface = "+surface);
        return 0;
    }
    public int calculateSquarePerimeter(int sideL, int sideR){
        int perimetr = 2*sideL+2*sideR;
        System.out.println("Perimetr = "+perimetr);
        return 0;
    }
}
